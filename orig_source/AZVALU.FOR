      SUBROUTINE AZVALU
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 3 ***
C...  THIS ROUTINE CALCULATES THE Z-COORDINATE
C     VALUE ON THE CURRENT ZSURF PLANE
C     FOR POINT DEFINITIONS YIELDING ONLY
C...  X- AND Y-COORDINATE VALUES
      IMPLICIT REAL*8(A-H,O-Z)
       DIMENSION DEFANS(82)
      COMMON/ADFSTO/DEFSTO(85),PARTNO(11)
      EQUIVALENCE(DEFSTO(4),DEFANS(1))
      COMMON/AZURF/ZSUR(4)
C
      DEFANS(3)=(ZSUR(4)-ZSUR(1)*DEFANS(1)-ZSUR(2)*DEFANS(2))/ZSUR(3)
      RETURN
      END

