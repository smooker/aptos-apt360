      SUBROUTINE AZSURF
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 3 ***
      IMPLICIT REAL*8(A-H,O-Z)
       COMMON/A1COM/REFSYS,NAME,NAME1,JSUBER,JSV,NAMSUB,KDBUG,INDXPT,
     1 LOOP,IFINI
      COMMON/ADFSTO/DEFSTO(85),PARTNO(11)
      COMMON/AZURF/ZSUR(4)
      COMMON/ADEBUG/IDEBUG(3),KCANDF
       DIMENSION IDFSTO(170)
       EQUIVALENCE (DEFSTO(1),IDFSTO(1))
       DATA A /8HAZSURF  /
C
C**** THIS ROUTINE PROCESSES PTPP CLASS 16 DATA(ZSURF/----). IT CALLS
C     AGTSRF TO MOVE THE SURFACE FROM CANON TO THE ZSUR TABLE
C
C     MOVE THE SURFACE
       CALL AGTARG(ZSUR,4,N)
C     IS THE SURFACE DEFINED
            IF(N.EQ.3) GO TO 20
C
C     THE SURFACE IS DEFINED.
C     IS THE DEFINITION REALLY A SURFACE
            IF(N.NE.2) GO TO 10
C
C     THE DEFINITION IS A SURFACE - CHECK FOR PLANE(SURFACE TYPE 3)
            IF(IDFSTO(1).EQ.3) GO TO 20
C
C     THE SURFACE IS NOT A PLANE
   10  JSUBER = 26
       DO 15  J = 1,4
   15  ZSUR(J) = 0.0
      GOTO 20
C
C     IS DEBUG ON
 20   IF(KDBUG.EQ.0) RETURN
       IDEBUG(1) = 12
       CALL ADPRNT(A)
      RETURN
      END

