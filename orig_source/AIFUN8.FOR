      SUBROUTINE AIFUN8(A,NAME)
C
C...  CONVERTS DOUBLE PRECISION FLOATING POINT NUMBER @A@ TO AN 8 BYTE
C...  EBCDIC INTEGER ( 6 DIGITS MAXIMUM ) WITH LEADING BLANKS,
C...  AND STORES THE RESULT IN ARGUMENT @NAME@
C
      REAL*8 A,B(1),NAME
      INTEGER*4 IPL(5)
      LOGICAL*1 LB(8),LC(10),IBL,IZ
      EQUIVALENCE(B(1),LB(1))
      DATA IPL/100000,10000,1000,100,10/
      DATA LC/ZF0,ZF1,ZF2,ZF3,ZF4,ZF5,ZF6,ZF7,ZF8,ZF9/
      DATA IBL/Z40/
C
C     CONVERT TO INTEGER
      IA=A
C     TRUNCATE
      IND=IA/1000000
      IA=IA-IND*1000000
C     LEADING ZEROS
      LB(1)=LC(1)
      LB(2)=LC(1)
      DO 10 I=1,5
      IND=IA/IPL(I)
      IA=IA-IND*IPL(I)
 10   LB(I+2)=LC(IND+1)
      LB(8)=LC(IA+1)
C     PUT IN LEADING BLANKS
      DO 20 I=1,7
      IF(LC(1).NE.LB(I)) GOTO 30
 20   LB(I)=IBL
 30   NAME=B(1)
      RETURN
      END

