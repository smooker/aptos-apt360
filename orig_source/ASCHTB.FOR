      SUBROUTINE ASCHTB(IFO,XS,YS,ER)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 2 ***
C
C.....SEARCH FOR TABCYL INTERVAL
C
C.......THIS VERSION OF SCHTB INCORPORATES 4Q65 LOGIC---LINES ADDED OR
C     ALTERED 12590032,34,36,90 ,100,110,120,150,160,180,190,200,215,220
C     250,260,270,280
      IMPLICIT REAL*8(A-H,O-Z)
      REAL*8 XS,YS,ER
       INTEGER PUNTAP
       COMMON/ATAPTB/PROTAP,CANTAP,CLTAPE,POCTAP,PLOTAP,SRFTAP,
     1 TAPES1,TAPES2,TAPES3,TAPES4,INTAPE,IOUTAP,PUNTAP
      COMMON/AVST/VST(7200)
      DIMENSION DEFTAB(1000)
      EQUIVALENCE(VST(7200),DEFTAB(1000))
      DIMENSION D( 999),ID(1998)
      EQUIVALENCE (DEFTAB(2),ID(1),D(1))
C***********************************************************************
      NAP=ID(2*IFO)-1
      IFO=IFO+11
      DO 11 I=1,NAP
            IF(DABS(D(IFO)-XS)-1.0E-2)  2, 2, 1
    2       IF(DABS(D(IFO+1)-YS)-1.0E-2)  8, 8, 1
    1 IFO=IFO+7
   11 CONTINUE
      IF(ER)13,111,13
  111 WRITE(IOUTAP,12)
   12 FORMAT(26H0TABCYL INTERVAL NOT FOUND)
   13 ER=1.0
      RETURN
    8 ER=0.0
      RETURN
      END

