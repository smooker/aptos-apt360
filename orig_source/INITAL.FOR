      SUBROUTINE INITAL
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 4 ***
C
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION IA(22),IP(40)
      REAL*8 INNAME
      DATA INNAME/'BENDIX  '/
C     *          *          *          *          *          *
C     SECTION ZERO TYPE STATEMENTS
      INTEGER PUNTAP
C     *          *          *          *          *          *
C     SECTION ZERO NAMED COMMON
      COMMON/ATIME/FLTIME,DATE,CLOCK(2)
      COMMON/A0CON/K0,K1,K2,K3,K4,K5,K6,K7,K8,K9,K10,K11,K12,IBLANK
      COMMON/ASISTM/IPTNLY,ITRCUT,IWAVEN,KFLAG1,LTVMIT,NCLREC,NOPOST,
     1NOPLOT,NUMPST,NUMPTR,IS3(3),KFLAGS(9)
      COMMON/APOSTP/PPNAME(20)
      COMMON/ATAPTB/PROTAP,CANTAP,CLTAPE,POCTAP,PLOTAP,SRFTAP,TAPES1,
     1TAPES2,TAPES3,TAPES4,INTAPE,IOUTAP,PUNTAP
C     *          *          *          *          *          *
      EQUIVALENCE(IA(1),IPTNLY),(PPNAME(1),IP(1))
C     *          *          *          *          *          *
C     END OF SECTION ZERO COMMON AND EQUIVALENCE STATEMENTS
C     *          *          *          *          *          *
C     REWIND UTILITY DATASETS
      CALL TAPEOP(PROTAP,1,IRET)
      CALL TAPEOP(CLTAPE,1,IRET)
      CALL TAPEOP(POCTAP,1,IRET)
      CALL TAPEOP(SRFTAP,1,IRET)
      CALL TAPEOP(TAPES4,1,IRET)
      CALL AFRESH
C     ZERO SYSTEM COMMON
      DO 10 I=1,22
      IA(I)=0
10    CONTINUE
      DO 20 I=1,40
      IP(I)=IBLANK
   20 CONTINUE
      KFLAGS(6)=IBLANK
C     TURN ON SECTION 1
      KFLAGS(1)=K1
C     TURN ON SECTION 2
      KFLAGS(2)=K1
C     TURN ON SECTION 4
      KFLAGS(5)=K1
      NUMPTR=K1
      NUMPST=K0
      PPNAME(1)=INNAME
      CALL ARTIME
      WRITE(IOUTAP,30)DATE,CLOCK(1),CLOCK(2)
   30 FORMAT('1',6X,'N/C 360 APT VERSION 4, MODIFICATION 4',5X,'DATE= ',
     1A6,' TIME OF DAY IN HRS./MIN./SEC IS ',A8,A3 ////)
      RETURN
      END

