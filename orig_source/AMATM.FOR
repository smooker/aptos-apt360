      SUBROUTINE AMATM(NSIZE)
      IMPLICIT REAL*8(A-H,O-Z)
       COMMON /ATMATY/XMAT4(16),XMAT3(16),XMAT2(16),XMAT1(16),TMATX(16)
       COMMON/A0CON/K0,K1,K2,K3,K4,K5,K6,K7,K8,K9,K10,K11,K12,IBLANK
    1 NS=NSIZE
    2 NTS=NS*NS
    3 J4=NS+K1
    4 J2=1-NS
    5 DO 19 J=1,NTS
    6 IF(J4-NS)11,7,7
    7 J2=J2+NS
    8 J3=J2+NS-K1
    9 J4=K1
   10 GO TO 12
   11 J4=J4+K1
   12 J5=J4
   13 SUM=0.
   14 DO 17 J1=J2,J3
   15 SUM=XMAT1(J1)*XMAT2(J5)+SUM
   16 J5=J5+NS
   17 CONTINUE
   18 XMAT3(J)=SUM
   19 CONTINUE
   20 RETURN
      END

