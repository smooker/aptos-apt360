      SUBROUTINE ACANCK(IT)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 3 ***
C...  THIS ROUTINE CHECKS CANON DEFINITIONS FOR GEOMETRIC TYPES
C     LINE,PLANE,CIRCLE,CYLNDR, AND SPHERE.
C...     ARGUMENT 'IT' IS THE GEOMETRIC SURFACE TYPE
C
      IMPLICIT REAL*8(A-H,O-Z)
       DIMENSION DEFANS(82)
       COMMON/A1COM/REFSYS,NAME,NAME1,JSUBER,JSV,NAMSUB,KDBUG,INDXPT,
     1 LOOP,IFINI
      COMMON/ADFSTO/DEFSTO(85),PARTNO(11)
      EQUIVALENCE(DEFSTO(4),DEFANS(1))
C
            IF(IT.LE.5) GO TO 10
            IF(IT-13)  90, 50, 90
   10  GO TO (90,20,20,60,60),IT
C
C     LINE OR PLANE - NORMALIZE THE EQUATION
   20 S = DSQRT(DEFANS(1)**2+DEFANS(2)**2+DEFANS(3)**2)
       DO 40  I = 1,3
      DEFANS(I)=DEFANS(I)/S
            IF(DABS(DEFANS(I))-.000001)  30, 30, 40
 30   DEFANS(I)=0.
 40   CONTINUE
      DEFANS(4)=DEFANS(4)/S
C
C     CHECK FOR ZERO VECTOR - IF SO, JSUBER WILL BE SET
      CALL AZVECT(DEFANS)
      GOTO 90
C
C     SPHERE - SET UP TO CHECK RADIUS
   50  DEFANS(7) = DEFANS(4)
      GOTO 70
C
C     CIRCLE OR CYLNDR - NORMALIZE AXIS VECTOR
 60   CALL ANORM(DEFANS(4),DEFANS(4))
C
C     CHECK FOR ZERO VECTOR - IF SO, JSUBER WILL BE SET
      CALL AZVECT(DEFANS(4))
C     CHECK RADIUS
   70       IF(DEFANS(7).GT.0) GO TO 90
C
       JSUBER = 1005
C
 90   RETURN
      END

