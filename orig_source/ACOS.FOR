      REAL FUNCTION ACOS*8(A)
C...  *** THIS PROGRAM CREATED FOR VERSION 4, MODIFICATION 0 ***
C
C...    ACOS IS ARC COSINE FUNCTION FOR VTLAXS.
C...    ARGUMENT (A) IS COSINE OF DESIRED ANGLE.
C...    ARC COSINE IS RETURNED IN RADIANS.
C...
       REAL*8 A,B
       B=DSQRT(1.-A**2)
       ACOS=DATAN(B/A)
       RETURN
       END

