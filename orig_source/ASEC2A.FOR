       SUBROUTINE ASEC2A (ISTX1,ISTX2)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 2 ***
      IMPLICIT REAL*8 (A-H,O-Z)
       COMMON /A2BURP/ ADENTC,ADENT,ADUMMY,ANS
      COMMON/A0CON/K0,K1,K2,K3,K4,K5,K6,K7,K8,K9,K10,K11,K12,IBLANK
      COMMON/ATAPTB/PROTAP,CANTAP,CLTAPE,POCTAP,PLOTAP,SRFTAP,TAPES1,
     1TAPES2,TAPES3,TAPES4,INTAPE,IOUTAP,PUNTAP
      COMMON/ASISTM/IPTNLY,ITRCUT,IWAVEN,KFLAG1,LTVMIT,NCLREC,NOPOST,
     1NOPLOT,NUMPST,NUMPTR,ICLPRT,INDEXX,IPLOTR,KFLAGS(9)
C... A2CMMN START
      COMMON/A2CMMN/ SP(3) ,SN(3)     ,TP(3)     ,TN(3)     ,SNK(3)    ,
     1SNL(3)    ,SPK(3)    ,SPL(3)    ,TNK(3)    ,TNL(3)    ,TPK(3)    ,
     2TPL(3)    ,PLNV(3)   ,CC(3)     ,U1(3)     ,CUTH(3)   ,SFVCT(3)  ,
     3VNDIR(3)  ,VMD(3)    ,VT(3)     ,TEE(3)    ,SLX(3)    ,DPLAN(3)  ,
     4S(1)      ,RC(1)     ,PLND(1)   ,TH(1)     ,CSD(1)    ,Z(1)      ,
     5EPS1(1)   ,EPS2(1)   ,EPS3(1)   ,EPS4(1)   ,EPS6(1)   ,EPS8(1)   ,
     6TAU(1)    ,TAU1(1)   ,TAU2(1)   ,STCK(1)   ,STCKN(1)  ,STCK1(1)  ,
     7STCK2(1)  ,COS1(1)   ,DPMX1(1)  ,CPLFL(1)  ,CPTST(1)  ,PLNCS(1)  ,
     8PLND1(1)  ,PLTST(1)  ,PAST(1)   ,SPAST(1)  ,ADCS(1)   ,H(1)      ,
     9CUTR(1) ,AFILLR(500) ,IAFL(1)   ,IPONTR(1) ,IFAR(1)   ,IPL(1)
      COMMON/A2CMMN/IUNFL(1),ISFTYP(1),ITLON(1)  ,ISVFL(1)  ,IFL4(1)   ,
     1IOP(1)    ,IOPSET(1) ,JIOPS(1)  ,NOTAN(1)  ,JENT(1)   ,JU1(1)    ,
     2ISIGCR(1) ,ITSEG(1)  ,JTLFLG(1) ,LOFS(1)   ,LIMFL(1)  ,ICSTR(1)  ,
     3JTN(1)    ,ICHKCT(1) ,J20(1)    ,J21(1)    ,J22(1)    ,J23(1)    ,
     4J24(1)    ,J50(1)    ,J51(1)    ,INDIR(1) ,IFILLR(209),TE(3)     ,
     5TEK(3)    ,TEL(3)    ,TES(3)    ,TA(3)     ,TAK(3)    ,TAL(3)    ,
     6PMOVE(3)  ,TI(3)     ,TIK(3)    ,TIL(3)    ,U2(3)     ,UVEC(3)   ,
     7VTEM(3)   ,CMOVE(3)  ,EX(3)     ,EY(3)     ,EZ(3)     ,VNUL(3)   ,
     8TM(3)     ,DIR(3)    ,AXIS(3)   ,AXIS1(3)  ,AXIS2(3)  ,AXIS3(3)  ,
     9REFPNT(3) ,RGT(3)    ,FWD(3)    ,UP(3)     ,ZLNORM(3) ,ZLPNT(3)
      COMMON/A2CMMN/TNDIR(3), VA(3)   ,TMVCMP(3) ,P1(3)     ,P2(3)     ,
     1PNT(3)    ,PNT1(3)   ,OLDU1(3)  ,OGLE(3)   ,RZERO(3)  ,TANMOT(3) ,
     2TLEND1(3) ,V(3)      ,CEND(3)   ,DELMOT(3) ,D2(3)     ,DNMOT(3)  ,
     3AX4(3)    ,A         ,B         ,C         ,RA        ,HI        ,
     4ALPHA     ,GAMMA     ,CAGL      ,SAGL      ,COM1      ,COM4      ,
     5COSTH     ,CTOCL     ,DIAM      ,DIF       ,DIFOUT    ,DINC      ,
     6DP        ,DPE       ,DPI       ,DPL       ,DPMAX     ,DP1       ,
     7DPCNT     ,DSMOV     ,D1        ,D2A       ,EL1       ,EL2       ,
     8FIPNT     ,FLIPCK    ,HAFDIA    ,HCHORD    ,OFFSET    ,PROD1     ,
     9PROD2     ,PSI       ,RADNO     ,RC1       ,RDRM      ,RDRN
      COMMON/A2CMMN/ RONE  ,RZEROL    ,SAVE1     ,SIUTH     ,TANGL     ,
     1THETA     ,ALP       ,EPS7      ,VAMAX     ,VL        ,ZDOTC     ,
     2ZGAMMA    ,ZIGN      ,ZL        ,ZL1       ,ZL2       ,ZNPTS     ,
     3TOLBND    ,CENDIS    ,CENLEN    ,CHORD     ,ABCD(2)   ,ACARD(2)  ,
     4SAVE(6)   ,CUTDAT(7) ,TEM(10)   ,TEMP(20)  ,TCDAT(260),QEQUIV(20),
     5IS        ,IIS       ,IT        ,IIT       ,IPS       ,IIPS      ,
     6IDS       ,IIDS      ,ICS       ,IICS      ,IGS       ,IIGS      ,
     7ICDEFL    ,ICDEFS    ,IAUTPS    ,IGO       ,ICRDCT    ,ICUT      ,
     8IGTFLG    ,INOPS     ,ISRCH     ,IOFSET    ,ISTRUP    ,I3DFLG    ,
     9MULOUT    ,MULTAX    ,NUMAX     ,NUMSFS    ,NUMSUR    ,IGOUG
      COMMON/A2CMMN/ IPDPL ,NUMDIM    ,IRSTRT    ,ICSCNT    ,I3        ,
     1I31       ,J         ,JA        ,JL        ,JS        ,JT        ,
     2KC        ,KND       ,KR        ,L         ,INCFS     ,NP        ,
     3IBEGIN    ,IFAR1     ,IK        ,INS1      ,ITNFLG    ,IVAMAX    ,
     4IWS       ,IIWS      ,IZ        ,NEXT1     ,NEXT2     ,NPTS      ,
     5NUMPT1    ,JAM       ,JAP       ,JTUSED    ,IFRL      ,N         ,
     6M         ,MDIC      ,JBR       ,JCKR      ,JCPR      ,JCR       ,
     7JDR       ,JGR       ,JMIN      ,JMINR     ,JPR       ,JPXR      ,
     8JTR       ,JCNT1     ,JCNT2     ,JCNT3     ,JSW       ,JTKF      ,
     9LISV      ,LSV       ,IPT       ,JFIND     ,IC        ,ICC
      COMMON/A2CMMN/  IL   ,K         ,NUMPTS    ,IAMDCT    ,IFL2      ,
     1J5        ,J6        ,JW6       ,J10       ,J12       ,J13       ,
     2J40       ,J43       ,J54       ,J103      ,PROBUF(100)          ,
     3CANSTO(2000) , IER   ,INSTR1    ,INSTR2    ,NWDS      ,ICL       ,
     4NREC      ,IRECNO    ,ITRFLG    ,NW        ,IDUMMY
C... A2CMMN END
      COMMON/A2DYDP/ KDYNFQ(12), NKFQ
      COMMON/A2CTDF/        TLHITE(1) ,TANHI(1)  ,TANLO(1)  ,UMAX(1)   ,
     1R1(1)     ,CORRAD(1) ,TCONT(1)  ,TLINC(65)
      DIMENSION  SINL(1), COSL(1), TLLDAT(1)
      EQUIVALENCE(SINL(1),TANHI(1)), (COSL(1),TANLO(1)), (TLLDAT(1),
     1TCONT(1))
      COMMON/A2FPC / Q15X01,QP9X01    ,QP6X01    ,QP5X01    ,QP4X01    ,
     1QP0001    ,QP001     ,QP01      ,QP1       ,Q1P       ,Q10P      ,
     2Q1000P    ,Q1PE5     ,Q1PE6     ,Q1PE10    ,Q1PE20    ,Q1PE30    ,
     3Q1PE36    ,Q1PE38    ,QP8X01    ,QP7X01    ,
     4QP4X09    ,QP9       ,QP99      ,QP995     ,QP997     ,QP999     ,
     5QP9999    ,QP4X95    ,QP5X95    ,QP6X9     ,QP7X9     ,
     6QP6X05    ,QP5X05    ,QP4X05    ,QP0005    ,QP005     ,QP05      ,
     7QP5       ,Q5P       ,                                 Q4X045    ,
     8QP0002    ,QP0047    ,QP017     ,Q1RAD     ,QP0349    ,QP125     ,
     9QP6       ,QP2       ,QP2499    ,QP25      ,QP3
      COMMON/A2FPC / QP7   ,QP75      ,QP8       ,QP866     ,Q1P01     ,
     1Q1P1      ,Q1P2      ,Q1P25     ,Q1P5      ,Q2P       ,Q2P5      ,
     2Q3P       ,Q3P5      ,Q4P       ,Q8P       ,Q11P      ,Q45P      ,
     3Q90P      ,Q130P     ,Q4500P    ,Q9000P    ,QP02      ,QNUL      ,
     4QPIE       ,QFIL(9)
C     *****
       IF (ISTX1.EQ.1) GO TO 5000
C     SPECIAL TREATMENT OF CODES 5000,6000,7000,8000,AND 11000 IN RESTRT
C
C
C     IN THE RESTART MODE, THERE ARE SEVERAL
C      VALID RESTART CONDITIONS
C     1. ANY FROM POINT
C      2. A GO TO/ FOLLOWED BY
C            A) ANOTHER GO TO/
C            B)A GO DELTA NOT ALONG THE TOOL AXIS
C            C) AN INDIR FOR THE DRIVE SRFACE
C            D A GO/ STATEMENT
C     3. ANY POCKET
C
C      NOTE... THE USEFULNESS OF THE GO TO/ IN RESTARTING  IS WIPED OUT
C               AN INTERVENING MOTION COMMAND OF THE TYPE GOXX/
C             OR A TRANTO, OR A MULTICS TRANTO
C      THE FOLLOWING CODE SETS IGTFLG = 1 WHENEVER A USEABLE GO
C      IS IN EFFECT, RESTARTS UNDER THE STATED CONDITIONS, AND
C      WIPES OUT THE IGTFLG AS DESCRIBED
C      GO DELTA AND INDIR VECTORS ARE CHECKED TO SEE THAT THEY DO NOT
C      LIE ALONG THE CUTTER AXIS
C
C     IS THIS A FROM -IF SO, CAN RESTART HERE
 5300 IF(INSTR2.NE.3) GOTO 5301
C     IS A FROM, RESET MODE AND IRSTRT
      ISRCH=1
      IGTFLG = 0
      GO TO 5000
 5301 GO TO (5310,5320,5330,5340,5350,5360),INSTR2
C      BRANCH ON TYPE OF RECORD
C      INDIRP
 5310 IF((IGTFLG.NE.1).OR.(PROBUF(1).EQ.ADUMMY)) GO TO 200
      CALL AVSUB(TE,PROBUF(3),VTEM)
      GO TO 5345
C     INDIRV
 5320 IF((IGTFLG.NE.1).OR.(PROBUF(1).EQ.ADUMMY)) GO TO 200
      CALL AVSTO(PROBUF(3),VTEM)
      GO TO 5345
C   STATEMENT SHOULD NEVER BE REACHED
 5330 CALL AERR(41)
C      GO DELTA
 5340 IF((IGTFLG.NE.1).OR.(NWDS.EQ.3)) GO TO 200
      CALL AVSTO(PROBUF(3),VTEM)
 5345 CALL AVNORM(VTEM,VTEM,IER)
      IF(IER.EQ.1) GO TO 5000
      CALL AVDOT(VTEM,TA,ANS)
      IF(DABS(ANS).GT.QP4X95) GO TO 5000
C   RESTART RIGHT HERE
      ISRCH = 1
      IGTFLG = 0
      IRSTRT = 0
      GO TO 5000
C      GO TO/    IF THIS IS THE SECOND IN A ROW, RESTART HERE
C      IF NOT, SET THE IGTFLG TO SHOW THAT THERE IS A USEABLE GO
 5350 IF(IGTFLG.EQ.1) GO TO 5355
      IGTFLG = 1
      GO TO 5000
 5355 ISRCH = 1
      IGTFLG = 0
      GO TO 5000
C      WARNING, COMPUTED CUTTER CENTERS ON PROTAPE
 5360 CALL AERR(-307)
 2000  ISTX2=K2
       RETURN
  200  ISTX2=K1
       RETURN
C     5000
 5000 GOTO (5010,5010,5030,5040,5050,5060,200,5010),INSTR2
C     INDIRP,INDIRV,SRFVCT HAVE SAME FORMAT
C     J INDEXES SURFACE,
C     K POINTS TO APPROPRIATE PROBUF WORD
 5010 J = 0
      K = -4
 5011 J = J+1
      K = K+5
      GO TO (5013,5012,5014,200),J
 5012 IT = IPS
      IIT = IIPS
      GOTO 5015
 5013 IT = IDS
      IIT = IIDS
      GOTO 5015
 5014 IT = ICS
      IIT = IICS
C     IF NO INFO GIVEN FOR THIS SURFACE, THE NAME IDUMMY WILL APPEAR
 5015 IF(PROBUF(K).EQ.ADUMMY) GOTO 5011
C     SEPARATE INDIRP(1),INDIRV(2) AND SRFVCT(8)
      IF(INSTR2-2)5020,5021,5022
C     INDIRP
C     SET UP UNDIR VECTOR
 5020 DO 5019  I=1,3
      NEXT1=IT+I-1
      NEXT2=K+I+1
 5019 VNDIR(NEXT1)=PROBUF(NEXT2)-TE(I)
      CALL AVNORM(VNDIR(IT),VNDIR(IT),IER)
      IF(IER.EQ.0) GO TO 5025
      CALL AERR(6)
 5025 INDIR(IIT) = 1
      IF(IT.NE.IDS) GO TO 5011
      CALL AVSTO(VNDIR(IT),TI)
      CALL AVSUB(TE,VNDIR(IT),TEK)
      GOTO 5011
C       INDIRV
 5021 CALL AVNORM(PROBUF(K+2),VNDIR(IT),IER)
      IF(IER.EQ.0) GO TO 5025
      CALL AERR(7)
C     SRFVCT
 5022 CALL AVNORM(PROBUF(K+2),SFVCT(IT),IER)
      IF(IER.NE.0) GO TO 5026
      ISVFL(IIT) = 1
      GOTO 5011
 5026 CALL AERR(8)
C
C
C     FROM POINT - ZERO OUT PREVIOUS CUTTER POSITION
 5030 CALL AVSTO(VNUL,TEK)
C      SET UP TYPE CODE ICL FRO ATAPE
      ICL = 3
C     SET CURRENT CUTTER LOCATION TO GIVEN POINT
      CALLAVSTO(PROBUF(3),TE)
C     SET UP NAME + SUBSCRIPT OF POINT
 5035 ABCD(1) = PROBUF(1)
      ABCD(2)=PROBUF(2)
C     GOTO COMMON AREA TO CHECK IF AXIS GIVEN AND UPDATE PMOVE
      GOTO 5100
C
C     GODLTA/V, GODLTA/N
 5040 CONTINUE
C      SET UP CL TAPE TYPE  ODE FOR ATAPE
      ICL = 5
C     SEE IF ONE PARAMETER OR 3 GIVEN BUT FIRST UPDATE TEK
      CALL AVSTO(TE,TEK)
      IF(NWDS.GT.3) GOTO 5045
C     NO MOVE IS ALONG AXIS
      DO 5041 I=1,3
 5041 TE(I) = TE(I) + PROBUF(3)*TA(I)
C     GO TO COMMON AREA TO CHECK TLAX + UPDATE PMOVE (WONT BE UPDATED IN
C     THIS CASE BECAUSE MOVE IS ALONG AXIS
      GO TO 5035
C     YES, MOVE IS VECTOR
 5045 CALLAVADD(PROBUF(3),TE,TE)
      GO TO 5035
C
C
C     GO TO POINT - UPDATE TEK (PREVIOUS CUTTER LOCATION)
 5050 CALLAVSTO(TE,TEK)
      ICL = 5
C     UPDATE CUTTER LOCATION
      CALLAVSTO(PROBUF(3),TE)
C     GO CHECK TOOL AXIS ETC
      GO TO 5035
C
C     SERIES OF POINTS - OUTPUT TO CLTAPE WITH WARNING
 5060 CALL AERR(-307)
      GOTO 2000
C
C
C     COMMON AREA NWDS INDICATES WHETHER TOOL AXIS WAS GIVEN ON INPUT
C     STATEMENT IF SO, SAVE OLD AND STORE NEW. SET UP TI VECTOR AS UNIT
C     VECTOR IN MOVEMENT DIRECTION, UPDATE PMOVE UNLESS MOVE IS ALONG
C     TOOL AXIS, AND OUTPUT POINT.
 5100 IF(NWDS.LE.5) GOTO 5110
C     TOOL AXIS GIVEN - SAVE OLD
 5102 CALLAVSTO(TA,TAK)
C     STORE NEW
      MULTAX = 0
      NUMAX = 3
      ICC = 0
      IF(INSTR1 .NE. 5000) GO TO 10
      IF(INSTR2 .NE. 4) GO TO 10
      IF(NWDS .EQ. 6) GO TO 5110
   10 CONTINUE
      CALL AVSTO(PROBUF(6),TA)
      CALL AVNORM(TA,TA,IER)
      IF(IER .EQ. 0) GO TO 5110
      CALL AERR(3)
C
C     SET UP TI
 5110 CALLAVSUB(TE,TEK,TI)
      CALL AVNORM(TI,TI,IER)
C     INSERT ERROR TEST HERE
      IF(IER .EQ. 1) GO TO 5112
      CALLAVDOT(TI,TA,ANS)
      IF(DABS(ANS).LE.QP997) CALL AVSTO(TI,PMOVE)
C     OUTPUT CUTTER LOCATION AND GO READ PROTAP
 5112 CONTINUE
      NW = 0
      CALL ASTOS
      CALL ATAPE
      GOTO 200
       END

