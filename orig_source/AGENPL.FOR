      SUBROUTINE AGENPL(V1,V2,PT)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 3 ***
      IMPLICIT REAL*8(A-H,O-Z)
      REAL*8 V1,V2,PT
C...  THIS ROUTINE ESTABLISHES A PLANE DEFINITION IN THE NORMAL FORM,
C     AX + BY + CZ = D
C...  FROM TWO NON-PARALLEL VECTORS AND A POINT ON THE PLANE
       DIMENSION DEFANS(82)
      COMMON/ADFSTO/DEFSTO(85),PARTNO(11)
      EQUIVALENCE(DEFSTO(4),DEFANS(1))
      DIMENSION VN(3),V1(3),V2(3),PT(3)
C...  CALCULATE NORMAL TO PLANE BY CROSSING V1 AND V2
      CALL ACROSS(V1,V2,VN)
C...  CHECK FOR ZERO VECTOR - IF SO, JSUBER IS SET
      CALL AZVECT(VN)
C...  NORMALIZE NORMAL - SAVE IN DEFANS
      CALL ANORM(VN,DEFANS)
C...  CALCULATE D VALUE
      CALL ADOT(DEFANS,PT,DEFANS(4))
      RETURN
      END

