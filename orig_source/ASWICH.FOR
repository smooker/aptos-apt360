       SUBROUTINE ASWICH(IT)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4,MODIFICATION 4 ***
      IMPLICIT REAL*8(A-H,O-Z)
       INTEGER IPT(600)
       COMMON/AMOTCM/PINT(30),IFURST,JPTIND,NOW,JSUB,KRESLT,KRSLT2
     1,NWDS,ITS,NEXT,MULTAX
      COMMON/APTPP/PT(300),INPTP,NW,JMODE,KLASS,KLASTP,IRECN,NPT,NPR
      COMMON/AVST/VST(2750),PTPP(2225),CANON(2225)
       DIMENSION ITEMP(2)
       EQUIVALENCE (TEMP,ITEMP(1),I),(ITEMP(2),J)
       EQUIVALENCE (PT(1),IPT(1))
C
C...     IF DATA TO BE PACKED IN ENTRY IS ZERO, JUST EXIT
            IF(IT.EQ.0) GO TO 200
C...     PICK UP PTPP ENTRY TYPE CODES
       TEMP = PT(NOW)
       JTEMP1 = 2*NOW - 1
C...     BRANCH ON ENTRY TYPE CODE TO APPROPRIATE TESTS
            IF(I.GE.6) GO TO 200
       GO TO (200,50,200,75,80),I
C
C...     CHANGE SCALAR TYPE CODE TO SURFACE, SINCE A SURFACE TYPE IS
C...     BEING PACKED
   50  IPT(JTEMP1) = 4
C...     TEST FOR VARIABLE SYMBOL OR EXPRESSION AS SUBSCRIPT
   75       IF(J.EQ.2) GO TO 90
            IF(J.EQ.3) GO TO 90
   80  IPT(JTEMP1+2) = IT
      RETURN
   90  IPT(JTEMP1+4) = 0
       IPT(JTEMP1+5) = IT
       INPTP = INPTP + 1
  200  RETURN
       END

