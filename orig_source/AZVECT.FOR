      SUBROUTINE AZVECT(V)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 1 ***
      IMPLICIT REAL*8(A-H,O-Z)
      REAL*8 V
C...  THIS ROUTINE CHECKS THE X,Y,Z COMPONENTS
C     OF A VECTOR FOR SOME ABSOLUTE VALUE LESS THAN .000001
C     IF SUCH IS THE CASE FOR ALL COMPONENTS,
C...  JSUBER IS SET TO 1005
       COMMON/A1COM/REFSYS,NAME,NAME1,JSUBER,JSV,NAMSUB,KDBUG,INDXPT,
     1 LOOP,IFINI
      DIMENSION V(3)
C
            IF(DABS(V(1))-.000001)  10, 10, 40
   10       IF(DABS(V(2))-.000001)  20, 20, 40
   20       IF(DABS(V(3))-.000001)  30, 30, 40
   30  JSUBER = 1005
 40   RETURN
      END

