      SUBROUTINE ADPRNT(ITITLE)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 4, MODIFICATION 3 ***
      IMPLICIT REAL*8(A-H,O-Z)
       COMMON /ATMATY/XMAT4(16),XMAT3(16),XMAT2(16),XMAT1(16),TMATX(16)
       DIMENSION DEFANS(82)
       DIMENSION PTPP(7200),CANON(7200)
       COMMON/A1COM/REFSYS,NAME,NAME1,JSUBER,JSV,NAMSUB,KDBUG,INDXPT,
     1 LOOP,IFINI
       REAL*8 PROTAP,CANTAP,CLTAPE,POCTAP,PLOTAP,SRFTAP,TAPES1,TAPES2,
     1 TAPES3,TAPES4,PPNAME
       INTEGER PUNTAP
       COMMON/ATAPTB/PROTAP,CANTAP,CLTAPE,POCTAP,PLOTAP,SRFTAP,
     1 TAPES1,TAPES2,TAPES3,TAPES4,INTAPE,IOUTAP,PUNTAP
      COMMON/AVST/VST(7200)
      DIMENSION DEFTAB(1000)
      EQUIVALENCE(VST(1),PTPP(1),CANON(1))
      EQUIVALENCE(VST(7200),DEFTAB(1000))
      COMMON/ASCALR/SCALR(180),ISCWS,NSURF,LOCTEM,IARGTP
      COMMON/A1PAS2/I,J,K,L,MOVLGE,KANMAX,ICANST,IOVREC,KANPTR,MDFTAB,
     1KANCNT,KAUX,KDFCNT,IDEFP,KPRCNT,MDFPRE,IPREP,KOVFLP,KSRFCT,KOVCNT,
     2LSCAN,IOVFLO,MAXCAN,IREF,ICANSC,NAMSTA,KANGO,LASPTP,ISUB,
     3LCOMP,LGEREC,IRECN,JREC,JNUM,KANCUR,KANCOT,KDFENT,KANPAR,LGELOC,
     4IDTMOV,ISPPRG,INDEXS,KPTP,KPTPCT,KLASTP,KLASCT,KPRNT
      COMMON/ADFSTO/DEFSTO(85),PARTNO(11)
      COMMON/AZURF/ZSUR(4)
      COMMON/APROTP/PROTP(102),PROSAV(102),NOWCLT,LSTPSV,INCORE
     1,JGORIT,SAVMOT
      COMMON/ADEBUG/IDEBUG(3),KCANDF
       DIMENSION ITITLE(2)
C
C**** THIS ROUTINE IS CALLED TO PRINT VARIOUS TABLES WHEN DEBUG FLAG IS
C     ON. THE INPUT IS THE VARIABLE ITITLE AND THE COMMON BLOCK ADEBUG.
C     IF ITITLE IS NON-ZERO IT CONTAINSTHE NAME OF THE PROGRAM WHICH
C     REQUESTED THE PRINTING.
C     THE FOLLOWING IS PRINTED FOR IDEBUG(1)
C     =1, THEXMAT1-XMAT4 AND TMATX AREAS
C     =2, THE  TMATX AREA.
C     =3,THE DEFSTO AREA FROM DEFSTO(1)THRU DEFSTO(IDEBUG(2)).
C     =4, THE CANON AREA FROM CANON(IDEBUG(2)) THRU CANON(IDEBUG(3)).
C     =5, THE DEFTAB AREA FROM DEFTAB(IDEBUG(2)) THRU DEFTAB(IDEBUG(3)
C     +IDEBUG(2)).
C     =6, THE TABCYL DEFPRE AREA FROM DEFPRE(IDEBUG(2)) THRU DEFPRE
C     (IDEBUG(3)+IDEBUG(2)).
C     =7, THE PTPP FROM PTPP(IDEBUG(2))THRU PTPP(IDEBUG(3)+IDEBUG(2)).
C     =8, THE VST1 AREA. THE VST1 AREA IS PRINTED IN PAIRS. IDEBUG(2)
C     CONTAINS THE INDEX TO THE FIRST PAIR AND IDEBUG(2) CONTAINS THE
C     NUMBER OF PAIRS.
C     =9,THE VST2 AREA FROM VST(IDEBUG(2)) THRU VST(IDEBUG(3)+IDEBUG(2))
C     =10,THE SURFTB FROM SURFTB(IDEBUG(2) THRU SURFTB(IDEBUG(3)+
C     IDEBUG(2)).
C     =11, THE SCALR AREA FROM SCALR(I DEBUG(2))THRU SCALR(IDEBUG(3)+
C     IDEBUG(2)).
C     =12,ZSUR AREA
C     =13,PROTP AND IRECN
C     IF IDEBUG(1) IS NOT IN THE INTEGER SET(1,13) ITITLE = NEGATIVE
C
C     TITLE FOR DEBUG PRINT
            IF(ITITLE(1).EQ.0) GO TO 20
      WRITE(IOUTAP,10) ITITLE(1),ITITLE(2)
   10 FORMAT(1H0,39X,25H*********DEBUG PRINT FOR ,A4,A4,10H *********)
   20 IF(IDEBUG( 1).GT. 13)GO TO 140
            IF(IDEBUG(1).LT.0) GO TO 140
       K = IDEBUG(2) + IDEBUG(3) - 1
      ID=IDEBUG( 1)
      ID1=IDEBUG( 2)
       ID2 = ID1 + 2*IDEBUG(3) - 1
       GO TO (30,40,50,60,70,80,90,100,150,150,150,170,180),ID
C
   30 WRITE(IOUTAP,35)(XMAT4(I),I=1,64)
   35 FORMAT(1H0,58X,7HXMAT1-4/(36X,4F12.9))
   40 WRITE(IOUTAP,45)(TMATX(I),I=1,16)
   45 FORMAT(1H0,58X,5HTMATX/(36X,4F12.9))
      GO TO 150
C
   50  WRITE (IOUTAP,55) (DEFSTO(I),I=1,ID1)
   55  FORMAT (1H0,57X,6HDEFSTO/4X,3Z20,3F16.9/(16X,6F16.9))
      GO TO 150
C
   60  WRITE (IOUTAP,65) (CANON(I),I=ID1,K)
   65  FORMAT (1H0,58X,5HCANON/4X,3Z20,3F16.9/(16X,6F16.9))
      GO TO 150
C
   70  WRITE (IOUTAP,75) (DEFTAB(I),I=1,K)
   75  FORMAT (1H0,57X,6HDEFTAB/(11X,5Z20))
      GO TO  150
C
   80  GO TO 150
C
   90  WRITE (IOUTAP,95) ID1,(PTPP(I),I=ID1,K)
   95  FORMAT (1H0,50X,9HPTPP FROM,I5/(11X,5Z20))
      GO TO 150
C
  100  ID2 = IDEBUG(3)
       WRITE (IOUTAP,105) VST(ID1),VST(ID1+1),CANON(ID2)
  105  FORMAT (1H0,58X,4HVST1/43X,A6,Z20,F16.9)
       GO TO 150
C
  140  CONTINUE
C
  150  RETURN
C
  170  WRITE (IOUTAP,175) (ZSUR(I),I=1,4)
  175  FORMAT (1H0,58X,4HZSUR/(30X,4F16.9))
      GOTO 150
C
  180  WRITE (IOUTAP,185) IRECN
      IF(ID1.EQ.0) GOTO 150
       WRITE (IOUTAP,190) (PROTP(II),II=1,ID1)
  185  FORMAT (1H0,58X,5HIRECN,I12)
  190 FORMAT(7X,5Z18)
      GO TO 150
C
      END

