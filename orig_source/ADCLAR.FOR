      SUBROUTINE ADCLAR(ILS,ILAST)
C     *** THIS PROGRAM LAST MODIFIED FOR VERSION 2, MODIFICATION 1 ***
      IMPLICIT REAL*8(A-H,O-Z)
      INTEGER PUNTAP
      REAL*8 INWORD,NEWONE,NAME,IDIS,MACNAM,KRFSYS
      INTEGER AMATOD,AEXTRA,AMDTOA,AEXTRD,AEXCH,DEBUG,SUM
      COMMON/A0CON/K0,K1,K2,K3,K4,K5,K6,K7,K8,K9,K10,K11,K12,IBLANK
      COMMON/A1COM/NAME,KRFSYS,SUM,JSUBER,NAMSUB,DEBUG,INDXPT,LOOP,IFINI
      COMMON/ATAPTB/PROTAP,CANTAP,CLTAPE,POCTAP,PLOTAP,SRFTAP,TAPES1
     1  ,TAPES2,TAPES3,TAPES4,INTAPE,IOUTAP,PUNTAP
      COMMON/AILMTB/ ELMENT(600),ICLASS(600),JLMENT
      COMMON/ADATA1/ANODEF,BLANX,BLANKS,SYN,TLAXIS,DUMMY,PLENT,LOOPST
     1 ,LOOPND,MACRO,NTRMAC,NCALL,LAPTH,JAPTH,IPLUS,NCOMMA,IFF,IC(10),
     2 LLASS
      IT = ILS + 2
  10  IF(IATYPE(IT).NE.4) GO TO 40
C...  PUNCT. MUST BE UNARY PLUS OR MINUS, OR ALLOWABLE MULTIPLE COMMA
      IF(ICLASS(IT).EQ.IC(2)) GO TO 20
      IF(ICLASS(IT) .EQ. IC(3)) GO TO 20
      IF(ICLASS(IT) .NE. IC(6)) GO TO 60
C...  FURTHER, MULTIPLE COMMAS ALLOWED ONLY FOR INDIRV AND INDIRP
      IF(AEXTRA(IBCLAS(ILS),2) .NE. 26) GO TO 60
      IF(AMDTOA(IBCLAS(ILS),2) .EQ. 9) GO TO 15
      IF(AMDTOA(IBCLAS(ILS),2) - 10)  60, 15, 60
  20  IT = IT + 1
  30  IF(IATYPE(IT).EQ.4) GO TO 60
  40  IF(IT.GE.ILAST) GO TO 70
      IT = IT + 1
C...  IS NEXT ENTRY PUNCTUATION
      IF(IATYPE(IT).NE.4) GO TO 40
C...  YES  -  THEN  IT MUST BE A COMMA
      IF(ICLASS(IT).NE.IC(6)) GO TO 60
C...  COMMA FOUND - CONTINUE VERIFYING STATEMENT
  15  IT=IT+1
      IF(IT .LE. ILAST)  GO TO 10
C...  A COMMA (SEPARATOR) IS EITHER MISSING OR INCORRECTLY USED
  60  JSUBER = 7
      IF(DEBUG .EQ. 0) RETURN
      WRITE(IOUTAP,65) IT
   65 FORMAT(1H 'ILLEGAL ENTRY AT ELEMENT TABLE ENTRY NUMBER' , I6 )
  70  RETURN
      END

